# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def flatten(self, root: Optional[TreeNode]) -> None:
        """
        Do not return anything, modify root in-place instead.
        """
        # å‰åºéå†ï¼Œå“ˆå¸Œè¡¨
        # æ—¶é—´å¤æ‚åº¦ï¼šO(n)
        # ç©ºé—´å¤æ‚åº¦ï¼šO(n)

        if root is None:
            return None
        hash_dict = []
        self.dfs(root, hash_dict)
        n = len(hash_dict)
        for i in range(n - 1):
            hash_dict[i].right = hash_dict[i + 1]
            hash_dict[i].left = None
        hash_dict[n - 1].right = None

    def dfs(self, node, hash_dict):
        if node is None:
            return

        hash_dict.append(node)
        self.dfs(node.left, hash_dict)
        self.dfs(node.right, hash_dict)
