class Solution:
    def maxDistance(self, nums1: List[int], nums2: List[int]) -> int:
        # äºŒåˆ†æŸ¥æ‰¾
        # æ—¶é—´å¤æ‚åº¦ï¼šO(nlogn)
        # ç©ºé—´å¤æ‚åº¦ï¼šO(1)
        # æ€è·¯ï¼šæ¯æ¬¡åœ¨nums2æ•°ç»„ä¸­ç”¨äºŒåˆ†æŸ¥æ‰¾å¤§äºnums1å½“å‰å€¼çš„æœ€å¤§å€¼ï¼Œè®¡ç®—å…¶è·ç¦»ï¼Œä¸æ–­æ›´æ–°

        nums1_len = len(nums1)
        nums2_len = len(nums2)
        result = 0
        for i in range(nums1_len):
            target = nums1[i]
            left = 0
            right = nums2_len - 1
            while left <= right:
                mid = left + (right - left) // 2
                if nums2[mid] >= target:
                    left = mid + 1
                else:
                    right = mid - 1
            if right >= i:
                result = max(result, right - i)
        return result
