class Solution:
    def findTargetSumWays(self, nums: List[int], target: int) -> int:
        # åŠ¨æ€è§„åˆ’
        # æ—¶é—´å¤æ‚åº¦ï¼šO(n * target_new)
        # ç©ºé—´å¤æ‚åº¦ï¼šO(target_new)

        n = len(nums)
        total = sum(nums)
        target_new = int((total + target) / 2)
        if (total + target) % 2 or abs(target) > total:
            return 0
        target_new = int(target_new)

        # dp[j]ï¼šæ„æˆjï¼Œæœ‰dp[j]ç§æ–¹æ³•
        dp = [0] * (target_new + 1)

        # åˆå§‹åŒ–
        dp[0] = 1

        # é€’å½’ éå†
        # left + right = sum  left - right = target  left=(sum+target)/2
        for i in range(n):
            for j in range(target_new, nums[i] - 1, -1):
                dp[j] += dp[j - nums[i]]
        return dp[target_new]
