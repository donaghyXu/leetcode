    def build(self, nums):
        if len(nums) == 0:
            return None

        mid = int(len(nums) / 2)
        root = TreeNode(val=nums[mid])
        if len(nums) == 1:
            return root

        root.left = self.build(nums[:mid])
        root.right = self.build(nums[mid+1:])
        return root
